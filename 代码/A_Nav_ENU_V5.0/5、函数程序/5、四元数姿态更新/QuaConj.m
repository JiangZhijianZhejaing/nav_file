function qout = QuaConj(qin)
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
% function：四元数共轭 
%
% Prototype: qout = QuaConj(qin)
% Inputs: qin - 四元数
%
% Output: qout - 共轭四元数
%
% By Taoran Zhao
% 2023/04/13 2023/05/18
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
% 见《惯性导航 (第三版) (秦永元 编著)》P255
